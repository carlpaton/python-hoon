# Simple math operations
print('--------- simple math operations ---------')
print(2 + 4)
print(2 - 4)
print(2 * 4)
print(2 / 4)

# Type action
print('--------- type action ---------')
print(type(2 / 4))

# Power of
print('--------- power of ---------')
print(2 ** 6)

# Devide rounded down
# Will cast the result to an int
print('--------- devide rounded down ---------')
print(2 // 4) # returns 0
print(5 // 4) # returns 1

# Modulo
# Calculates the remainder of this division
print('--------- modus ---------')
print(5 % 2) # returns 1
